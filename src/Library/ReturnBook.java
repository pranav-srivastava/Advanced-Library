    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql = "select * from issue_book where book_id = ?";
        try{
            pst = con.prepareStatement(sql);
            try{
                if(jTextField7.getText().equals(""))
                    JOptionPane.showMessageDialog(null, "Please Enter Book_Id");
                else{
                    pst.setInt(1, Integer.parseInt(jTextField7.getText()));
                    rs = pst.executeQuery();
                    if(rs.next()){
                        jTextField9.setText(rs.getString("Name"));
                        jTextField10.setText(rs.getString("Edition"));
                        jTextField11.setText(rs.getString("Publisher"));
                        jTextField12.setText(rs.getString("Price"));
                        jTextField13.setText(rs.getString("Pages"));
                        jTextField14.setText(rs.getString("Date_Of_Issue"));
                        jTextField1.setText(rs.getString("Student_ID"));
                        jTextField2.setText(rs.getString("St_Name"));
                        jTextField3.setText(rs.getString("Father_Name"));
                        jTextField4.setText(rs.getString("Course"));
                        jTextField5.setText(rs.getString("Branch"));
                        jTextField6.setText(rs.getString("Year"));
                        jTextField8.setText(rs.getString("Semester"));
                        
                        rs.close();
                        pst.close();
                    }else
                        JOptionPane.showMessageDialog(null, "This Book Is Not Issued");
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Book_ID is an integer");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{ if(rs!=null)rs.close(); }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            try{ if(pst!=null)pst.close(); }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    public void delete(){
        String sql = "delete from issue_book where book_id = ?";
        try{
            pst = con.prepareStatement(sql);
            pst.setString(1, jTextField7.getText());
            pst.execute();
            pst.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{ if(pst!=null)pst.close(); }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
    
    public void returnTheBook(){
        String sql = "insert into return_book(Student_ID,St_Name,Father_Name,Course,Branch,Year,Semester,Date_Of_Issue,Book_Id,Bk_Name,Edition,Publisher,Price,Pages,Date_Of_Return)values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)";
        try{
            pst = con.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(jTextField1.getText()));
            pst.setString(2, jTextField2.getText());
            pst.setString(3, jTextField3.getText());
            pst.setString(4, jTextField4.getText());
            pst.setString(5, jTextField5.getText());
            pst.setString(6, jTextField6.getText());
            pst.setString(7, jTextField8.getText());
            pst.setString(8, jTextField14.getText());
            pst.setInt(9, Integer.parseInt(jTextField7.getText()));
            pst.setString(10, jTextField9.getText());
            pst.setString(11, jTextField10.getText());
            pst.setString(12, jTextField11.getText());
            pst.setInt(13, Integer.parseInt(jTextField12.getText()));
            pst.setInt(14, Integer.parseInt(jTextField13.getText()));
            pst.setString(15, ((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Book has been returned successfully");
            pst.close();
            con.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{ if(pst!=null)pst.close(); }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
    
