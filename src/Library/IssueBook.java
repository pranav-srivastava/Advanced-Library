    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql = "select * from book where book_id = ?";
        try{
            pst = con.prepareStatement(sql);
            try{
                if(jTextField1.getText().equals(""))
                        JOptionPane.showMessageDialog(null, "Please Enter Book_ID");
                else{
                    int bk_id = Integer.parseInt(jTextField1.getText());
                    pst.setInt(1, bk_id);
                    rs = pst.executeQuery();
                    if(rs.next()){
                        jTextField2.setText(rs.getString("Name")); // or rs.getString(2); // here 2 denotes 2nd parameter in book database.
                        jTextField3.setText(rs.getString("Edition"));
                        jTextField4.setText(rs.getString("Publisher"));
                        jTextField5.setText(rs.getString("Price"));
                        jTextField6.setText(rs.getString("Pages"));
                    }
                    else
                        JOptionPane.showMessageDialog(null, "Book_ID Not Found !!");
                    rs.close();
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Book_ID is an integer.");
            }
            pst.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{ if(rs!=null)rs.close(); }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            try{ if(pst!=null)pst.close(); }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

