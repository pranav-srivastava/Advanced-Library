    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql = "insert into Student(Student_ID,Name,Father_Name,Course,Branch,Year,Semester) values (?,?,?,?,?,?,?)";
        try{
            pst = con.prepareStatement(sql);
            if(jTextField1.getText().equals("") || jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextField4.getText().equals(""))
                JOptionPane.showMessageDialog(null, "Please fill all the fields.");
            else{
                pst.setInt(1, Integer.parseInt(jTextField1.getText()));
                pst.setString(2, jTextField2.getText());
                pst.setString(3, jTextField3.getText());
                pst.setString(4, (String) jComboBox1.getSelectedItem());
                pst.setString(5, jTextField4.getText());
                pst.setString(6, (String) jComboBox2.getSelectedItem());
                pst.setString(7, (String) jComboBox3.getSelectedItem());
                pst.execute();
                
                JOptionPane.showMessageDialog(null, "New Student Successfully Registered");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                pst.close();
                con.close();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{ if(pst!=null)pst.close(); }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

