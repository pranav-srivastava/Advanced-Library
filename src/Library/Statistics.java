    public void issue_book_detail(){
        String sql = "select Book_ID,Name,Edition,Publisher,Price,Pages from issue_book";
        try{
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));
            rs.close();
            pst.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{ if(rs!=null)rs.close(); }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            try{ if(pst!=null)pst.close(); }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
    
