    public void Search(){
        String txt1 = jTextField1.getText();
        String sql = "select * from account where Username = '"+ txt1 +"'" ;
        try{
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(4));
                
            }
            else 
                JOptionPane.showMessageDialog(null, "Incorrect Username");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        finally{                // here finally is used as connection is not closed i.e. con.close() doesn't happen here.
            try{ if(rs!=null) rs.close();}
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            try{ if(pst!=null) pst.close();}
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
    
    public void Retrieve(){
        String user = jTextField1.getText();
        String ans = jTextField4.getText();
        String sql = "select * from account where Username = '" + user +"'" + " and Answer = '"+ans +"'";
        try{
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            if(jTextField2.getText().equals("") || jTextField3.getText().equals(""))
                JOptionPane.showMessageDialog(null, "First search for the username !!");
            else if(rs.next()){
                jTextField5.setText(rs.getString(3));
                pst.close();
                rs.close();
                con.close();
            }
            else
                JOptionPane.showMessageDialog(null,"Wrong Answer !!");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        finally{
            try{ if(pst!=null)pst.close(); }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
            try{ if(rs!=null)rs.close(); }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
        }
    }

