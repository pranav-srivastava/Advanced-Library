    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String sql = "insert into book(Book_ID,Name,Edition,Publisher,Price,Pages) values (?,?,?,?,?,?)";
        try{
            pst = con.prepareStatement(sql);
            
            if(jTextField1.getText().equals("") || jTextField2.getText().equals("") || jTextField3.getText().equals("") || jTextField6.getText().equals("") || jTextField5.getText().equals(""))
                JOptionPane.showMessageDialog(null, "Please Fill All The Fields !!");
            
            else{
                try{
                pst.setInt(1, Integer.parseInt(jTextField1.getText()));
                pst.setString(2, jTextField2.getText());
                pst.setString(3, (String) jComboBox1.getSelectedItem());    // jComboBox must be String
                pst.setString(4, jTextField3.getText());
                pst.setInt(5, Integer.parseInt(jTextField6.getText()));
                pst.setInt(6, Integer.parseInt(jTextField5.getText()));
                pst.execute();
                JOptionPane.showMessageDialog(null, "New Book Added Successfully.");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField6.setText("");
                jTextField5.setText("");
                pst.close();
                con.close();
                }catch(Exception e){
                        JOptionPane.showMessageDialog(null, "Please enter only integers in price field and pages field.");
                }
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{ if(pst!=null)pst.close(); }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

